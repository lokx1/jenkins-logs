/* Data Types */
#include <stdio.h>

int main() {
    int integerVar = 10;
    float floatVar = 3.14;
    char charVar = 'A';

    printf("Integer: %d\n", integerVar);
    printf("Float: %f\n", floatVar);
    printf("Character: %c\n", charVar);

    return 0;
}
