// Write a  C Program to Swap Numbers without Using Temporary Variable

#include <stdio.h>

int main(){
	printf("\n ======================= SWAP 2 NUMBERS WITHOUT USING TEMPORARY VARIABLES ====================== \n"); 
	float a, b;
	printf("\n Enter the first number [X] : "); 
	scanf("%f", &a);
	printf("\n Enter the second number [Y] : "); 
	scanf("%f", &b);
	printf("\n First Number [X] = %.3f\n Second Number [Y] = %.3f\n", b, a); 
	return 0;
}
